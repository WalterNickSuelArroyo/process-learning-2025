<?php

// El scope de una variable es el contexto en el que se declara la variable.
// En PHP, el scope de una variable se define por la posición en la que se declara la variable.
// PHP tiene tres ámbitos de variables diferentes:
// local
// global
// estático
// Una variable declarada fuera de una función tiene un ámbito global y solo se puede acceder a ella fuera de una función.
// Una variable declarada dentro de una función tiene un ámbito local y solo se puede acceder a ella dentro de esa función.
// Una variable declarada dentro de una función con la palabra clave static tiene un ámbito estático y solo se puede acceder a ella dentro de esa función.
// La diferencia entre los ámbitos local y estático es que el ámbito local se destruye al finalizar la función, mientras que el ámbito estático no.

?>