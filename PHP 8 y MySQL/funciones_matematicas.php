<?php

$numero = 15.785;

// round redondea el número
echo round($numero, 2); 
// salida: 15.79

echo "<br>";

// rand genera un número aleatorio
echo rand(1, 10);
// salida: número aleatorio entre 1 y 10

echo "<br>";

// ceil redondea siempre hacia arriba
echo ceil(12.1);
// salida: 13

echo "<br>";

// floor redondea siempre hacia abajo
echo floor(12.9);
// salida: 12

echo "<br>";

// sqrt calcula la raíz cuadrada
echo sqrt(16);
// salida: 4

echo "<br>";

// pow calcula la potencia
echo pow(2, 3);
// salida: 8

echo "<br>";

// pi devuelve el valor de pi
echo pi();
// salida: 3.1415926535898

echo "<br>";

// M_PI devuelve el valor de pi
echo M_PI
?>