<?php

$mes = 'Julio';

switch ($mes) {
    case 'Diciembre':
        echo 'Feliz Navidad';
        break;
    case 'Enero':
        echo 'Feliz Año Nuevo';
        break;
    case 'Julio':
        echo 'Felices Vacaciones';
        break;
    default:
        echo 'En este mes no se celebra nada';
        break;
}

?>